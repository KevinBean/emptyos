def resolve_vault_path(explicit: str | None = None) -> Path | None:
    conn_file = PROJECT_ROOT / ".claude" / "vault-connection.json"
    if explicit:
        return Path(explicit)
    if conn_file.exists():
        data = json.loads(conn_file.read_text())
        if data.get("connected") and data.get("vault_path"):
            return Path(data["vault_path"])
    toml_file = PROJECT_ROOT / "emptyos.toml"
    if toml_file.exists():
        with open(toml_file, "rb") as f:
            cfg = tomllib.load(f)
        p = cfg.get("notes", {}).get("path", "")
        if p:
            return Path(p)
    return None


def scan_manifests(base_dirs: list[Path], key: str = "app") -> list[dict]:
    results = []
    for base in base_dirs:
        if not base.exists():
            continue
        for mf in sorted(base.glob("*/manifest.toml")):
            if "_retired" in str(mf) or "_example" in str(mf):
                continue
            try:
                with open(mf, "rb") as f:
                    data = tomllib.load(f)
                entry = data.get(key, {})
                entry["_path"] = mf.parent
                entry["_has_ui"] = (mf.parent / "pages" / "index.html").exists()
                entry["_provides"] = data.get("provides", {})
                entry["_requires"] = data.get("requires", {})
                results.append(entry)
            except Exception:
                continue
    return results


def count_endpoints() -> int:
    count = 0
    for pyfile in PROJECT_ROOT.rglob("*.py"):
        rel = pyfile.relative_to(PROJECT_ROOT)
        parts = rel.parts
        if parts[0] not in ("apps", "plugins"):
            continue
        if "_retired" in str(pyfile):
            continue
        try:
            text = pyfile.read_text(encoding="utf-8", errors="ignore")
            count += text.count("@web_route")
        except Exception:
            continue
    return count


def categorize_app(app_id: str) -> str | None:
    for cat, ids in APP_CATEGORIES.items():
        if app_id in ids:
            return cat
    return None


def generate_apps_md(apps: list[dict]) -> str:
    categorized: dict[str, list[dict]] = {}
    uncategorized = []
    for app in apps:
        cat = categorize_app(app.get("id", ""))
        if cat:
            categorized.setdefault(cat, []).append(app)
        else:
            uncategorized.append(app)

    total = len(apps)
    lines = [
        "---",
        "title: App Catalog",
        "type: page",
        "publish: true",
        "nav_order: 3",
        "nav_label: Apps",
        f"summary: All {total} apps organized by category — infrastructure, productivity, creative, voice, and system",
        "---",
        "",
        AUTOGEN_HEADER,
        "",
    ]

    cat_order = list(APP_CATEGORIES.keys())
    for cat in cat_order:
        cat_apps = categorized.get(cat, [])
        if not cat_apps:
            continue
        lines.append(f"## {cat}")
        lines.append("")
        lines.append("| App | What It Does |")
        lines.append("|---|---|")
        for app in cat_apps:
            name = f"**{app.get('id', '?')}**"
            desc = app.get("description", "").rstrip(".")
            lines.append(f"| {name} | {desc} |")
        lines.append("")

    if uncategorized:
        lines.append("## Other")
        lines.append("")
        lines.append("| App | What It Does |")
        lines.append("|---|---|")
        for app in uncategorized:
            name = f"**{app.get('id', '?')}**"
            desc = app.get("description", "").rstrip(".")
            lines.append(f"| {name} | {desc} |")
        lines.append("")

    lines.append("## The Pattern")
    lines.append("")
    lines.append("Every app follows the same structure:")
    lines.append("")
    lines.append("```")
    lines.append("apps/{id}/")
    lines.append("├── manifest.toml    # Declares id, capabilities, events, routes")
    lines.append("├── app.py           # Python class extending BaseApp")
    lines.append("├── __init__.py")
    lines.append("└── pages/           # HTML files for custom UI (optional)")
    lines.append("    └── index.html")
    lines.append("```")
    lines.append("")
    lines.append(
        "Apps without `pages/` get an auto-generated UI. Apps with custom pages get hot-reloaded HTML — edit and refresh, no restart needed."
    )
    lines.append("")
    return "\n".join(lines)


