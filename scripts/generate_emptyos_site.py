def inject_stats(content: str, stats_block: str) -> str:
    if STATS_START in content and STATS_END in content:
        pattern = re.compile(
            re.escape(STATS_START) + r".*?" + re.escape(STATS_END),
            re.DOTALL,
        )
        replacement = f"{STATS_START}\n{stats_block}\n{STATS_END}"
        return pattern.sub(replacement, content)
    return content


def fetch_integrity_audit() -> dict | None:
    """Fetch current integrity score from a running daemon.

    Returns None if the daemon isn't reachable — graceful degradation so the
    script still works offline. Also returns None on malformed responses.
    """
    try:
        import urllib.request

        with urllib.request.urlopen(
            "http://localhost:9000/integrity/api/audit",
            timeout=3,
        ) as resp:
            data = json.loads(resp.read())
            if isinstance(data, dict) and "total_score" in data:
                return data
    except Exception:
        pass
    return None


def build_stats_block(apps: list[dict], plugins: list[dict], endpoints: int) -> str:
    total_apps = len(apps)
    plugin_count = len(plugins)
    cap_count = len(CAPABILITIES)

    audit = fetch_integrity_audit()
    if audit is None:
        print(
            "  note: integrity audit unreachable (daemon offline?) — score badge omitted",
            file=sys.stderr,
        )

    pct = audit.get("pct", 0) if audit else None

    metric_parts = [f"{total_apps} Apps"]
    if pct is not None:
        metric_parts.append(f"{pct}% Integrity")
    metric_parts += [f"{cap_count} Capabilities", f"{plugin_count} Plugins"]
    metric_line = "<!-- metrics: " + " · ".join(metric_parts) + " -->"

    return metric_line + f"\n\n_Generated {date.today().isoformat()} from live system state._"


def update_architecture_counts(content: str, app_count: int) -> str:
    content = re.sub(
        r"Apps \(\d+, all first-class\)",
        f"Apps ({app_count}, all first-class)",
        content,
    )
    content = re.sub(
        r"all \d+ apps",
        f"all {app_count} apps",
        content,
    )
    return content


def main():
    parser = argparse.ArgumentParser(description="Generate EmptyOS site pages")
    parser.add_argument("--dry-run", action="store_true", help="Show changes without writing")
    parser.add_argument("--vault", type=str, help="Vault path override")
    parser.add_argument(
        "--include-personal",
        action="store_true",
        help="Scan apps/personal/ too (default: community apps only — "
        "the public site should not list machine-specific apps)",
    )
    args = parser.parse_args()

    vault = resolve_vault_path(args.vault)
    if not vault or not vault.exists():
        print(f"ERROR: vault path not found: {vault}", file=sys.stderr)
        sys.exit(1)

    site_dir = vault / "30_Resources" / "EmptyOS-Site"
    if not site_dir.exists():
        print(f"ERROR: site source dir not found: {site_dir}", file=sys.stderr)
        sys.exit(1)

    app_scan_dirs = [PROJECT_ROOT / "apps"]
    if args.include_personal:
        app_scan_dirs.append(PROJECT_ROOT / "apps" / "personal")
    apps = scan_manifests(app_scan_dirs, key="app")

    plugins = scan_manifests([PROJECT_ROOT / "plugins"], key="plugin")

    endpoints = count_endpoints()

    print(f"Scanned: {len(apps)} apps, {len(plugins)} plugins, {endpoints} endpoints")

    changes = []

    # --- apps.md (fully generated) ---
    apps_md = generate_apps_md(apps)
    apps_file = site_dir / "apps.md"
    old = apps_file.read_text(encoding="utf-8") if apps_file.exists() else ""
    if apps_md != old:
        changes.append(("apps.md", apps_md, old))

    # --- plugins.md (fully generated) ---
    plugins_md = generate_plugins_md(plugins)
    plugins_file = site_dir / "plugins.md"
    old = plugins_file.read_text(encoding="utf-8") if plugins_file.exists() else ""
    if plugins_md != old:
        changes.append(("plugins.md", plugins_md, old))

    # --- capabilities.md (new, generated) ---
    caps_md = generate_capabilities_md()
    caps_file = site_dir / "capabilities.md"
    old = caps_file.read_text(encoding="utf-8") if caps_file.exists() else ""
    if caps_md != old:
        changes.append(("capabilities.md", caps_md, old))

    # --- index.md (inject stats block) ---
    index_file = site_dir / "index.md"
    if index_file.exists():
        index_content = index_file.read_text(encoding="utf-8")
        stats_block = build_stats_block(apps, plugins, endpoints)
        if STATS_START in index_content:
            new_content = inject_stats(index_content, stats_block)
        else:
            new_content = index_content
        if new_content != index_content:
            changes.append(("index.md", new_content, index_content))

    # --- architecture.md (update app count in ASCII art) ---
    arch_file = site_dir / "architecture.md"
    if arch_file.exists():
        arch_content = arch_file.read_text(encoding="utf-8")
        new_arch = update_architecture_counts(arch_content, len(apps))
        if new_arch != arch_content:
            changes.append(("architecture.md", new_arch, arch_content))

    if not changes:
        print("No changes needed — site is up to date.")
        return

    print(f"\n{'DRY RUN — ' if args.dry_run else ''}{len(changes)} file(s) to update:")
    for name, new, old in changes:
        old_lines = len(old.splitlines()) if old else 0
        new_lines = len(new.splitlines())
        print(f"  {name}: {old_lines} -> {new_lines} lines")

    if args.dry_run:
        print("\nRe-run without --dry-run to apply.")
        return

    for name, new, _old in changes:
        target = site_dir / name
        target.write_text(new, encoding="utf-8")
        print(f"  OK {name}")

    print(f"\nDone. {len(changes)} file(s) updated in {site_dir}")
    print("Run 'POST /publish/api/build' or use the Publish app to rebuild the site.")


if __name__ == "__main__":
    main()
